#include "3-calc.h"

/**
 * op_add - Addition function
 * @a: first number to add
 * @b: second number to add to a
 *
 * Return: a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Substraction function
 * @a: first number
 * @b: second number to substract from a
 *
 * Return: a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Multiplication function
 * @a: first number
 * @b: second number to multiply by a
 *
 * Return: a * b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Division function
 * @a: first number
 * @b: second number to divide by a
 *
 * Return: a / b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Remainder function
 * @a: first number
 * @b: second number
 *
 * Return: a % b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
